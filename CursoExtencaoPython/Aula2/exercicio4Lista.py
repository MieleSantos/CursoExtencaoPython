lista = [5,7,2,9,4,1,3]
print("Tamando da lista->",len(lista))
print("Maior valor->",max(lista))
print("Menor valor->",min(lista))
print("Soma de todos os elementos->",sum(lista))
print("testanto o sorted->",sorted(lista))#sorted retorna a lista ordenada
lista.sort()#ordena lista em ordem crescente
print("Ordem crescente->",lista)
lista.reverse()#invert os elementos
print("Ordem decresce->",lista)

